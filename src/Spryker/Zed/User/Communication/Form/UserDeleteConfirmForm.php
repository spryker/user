namespace Spryker\Zed\User\Communication\Form;

use Spryker\Zed\Kernel\Communication\Form\AbstractType;
use Symfony\Component\Form\FormBuilderInterface;
use Symfony\Component\OptionsResolver\OptionsResolver;

/**
 * @method \Spryker\Zed\User\UserConfig getConfig()
 * @method \Spryker\Zed\User\Persistence\UserQueryContainerInterface getQueryContainer()
 * @method \Spryker\Zed\User\Business\UserFacadeInterface getFacade()
 * @method \Spryker\Zed\User\Communication\UserCommunicationFactory getFactory()
 * @method \Spryker\Zed\User\Persistence\UserRepositoryInterface getRepository()
 */
class UserDeleteConfirmForm extends AbstractType
{
    /**
     * @see \Spryker\Zed\User\Communication\Controller\EditController::deleteAction()
     *
     * @var string
     */
    protected const DELETE_USER_URL = '/user/edit/delete';

    /**
     * @var string
     */
    protected const DELETE_METHOD = 'DELETE';

    /**
     * @return string
     */
    public function getBlockPrefix(): string
    {
        return 'delete_confirm_form';
    }

    /**
     * @param \Symfony\Component\Form\FormBuilderInterface $builder
     * @param array<string, mixed> $options
     *
     * @return void
     */
    public function buildForm(FormBuilderInterface $builder, array $options): void
    {
        $builder->setAction(static::DELETE_USER_URL);
        $builder->setMethod(static::DELETE_METHOD);
    }

    /**
     * @param \Symfony\Component\OptionsResolver\OptionsResolver $resolver
     *
     * @return void
     */
    public function configureOptions(OptionsResolver $resolver): void
    {
        $resolver->setDefaults([
            'attr' => [
                'class' => 'form-inline',
            ],
        ]);
    }
}
